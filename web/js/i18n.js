export const LOCALES = { en: "en-GB", es: "es-ES", fr: "fr-FR", pt: "pt-BR" };

export function localizeLine(line, language = "en") {
  if (language === "en" || !line.semanticKey) return line.text;
  const template = P[language]?.[line.semanticKey];
  return template ? fill(template, line.args || {}) : line.text;
}

export function describePressure(moment, state, language = "en") {
  const team = moment.team === "home" ? state.home.name : state.away.name;
  const other = moment.team === "home" ? state.away.name : state.home.name;
  const high = moment.detail?.level === "high";
  const phrases = {
    en: high ? `${team} enter a high-danger phase near ${other}'s goal.` : `${team} sustain attacking possession.`,
    es: high ? `${team} entra en zona de máximo peligro cerca del área de ${other}.` : `${team} mantiene la posesión en ataque.`,
    fr: high ? `${team} entre dans une phase de grand danger près du but de ${other}.` : `${team} maintient la pression offensive.`,
    pt: high ? `${team} entra em uma fase de alto perigo perto do gol de ${other}.` : `${team} mantém a posse no ataque.`,
  };
  return phrases[language] || phrases.en;
}

function fill(template, args) {
  return template.replace(/\{(\w+)\}/g, (_, key) => args[key] ?? "");
}
